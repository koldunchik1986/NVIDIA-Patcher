#!/bin/bash
#
# Утилита для идентификации майнинговых видеокарт NVIDIA
#

echo -e '\033[0;32mИдентификатор майнинговых карт NVIDIA\033[0m'
echo '====================================='

# Поиск майнинговых карт
echo 'Поиск майнинговых карт...'
if lspci -nn | grep -i nvidia | grep -E '1B80|1C02|1BA1|1C03' > /dev/null; then
    echo -e '\033[0;32mНайдены майнинговые карты!\033[0m'
    lspci -nn | grep -i nvidia | grep -E '1B80|1C02|1BA1|1C03'
else
    echo 'Майнинговые карты не найдены'
fi

echo ''
echo 'Анализ завершен!'
